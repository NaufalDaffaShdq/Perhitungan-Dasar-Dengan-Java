package KumpulanContoh;

public class Contoh2 {
    
    public static void main(String [] args)
    {
        int t = 7;
        int b = 5;
        int h = b + 2;
        t = b + h;
        int a = b - t;
        System.out.println(" t  = "+t);
        System.out.println(" t + b = "+h);
        System.out.println(" b - t  = "+a);
        System.out.println(" b  = "+b);
    }
}